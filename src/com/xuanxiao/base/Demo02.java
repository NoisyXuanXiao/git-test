package com.xuanxiao.base;

public class Demo02 {
    public static void main(String[] args) {
        // 八大基本数据类型

        // 整数
        int num1 = 10;
        byte num2 = 20;
        short num3 = 30;
        long mum4 = 30L; // long类型后面要加L

        // 小数
        float num5 = 50.1F; // float类型后面要加F
        double num6 = 3.1414532216456461;

        // 字符
        char name1 = 'A';

        // 字符串
        String name2 = "孙雨轩";

        // 布尔型
        Boolean flag = true;

    }
}
