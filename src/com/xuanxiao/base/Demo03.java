package com.xuanxiao.base;

public class Demo03 {
    public static void main(String[] args) {

        // 整数进制
        int num1=  10;
        int num2 = 0b10; // 二进制0b
        int num3 = 010; // 八进制0
        int num4 = 0x10; // 十六进制0x

        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);


    }
}
